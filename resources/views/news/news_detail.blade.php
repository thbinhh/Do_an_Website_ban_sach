						<p><strong>Cuốn sách&nbsp;Xa ngoài kia nơi loài tôm hát</strong>&nbsp;(Tên gốc:&nbsp;Where the Crawdads Sing)&nbsp;tiểu thuyết đầu tay của nhà văn Delia Owens được xuất bản lần đầu năm 2018, nhưng đã nhanh chóng chiếm được sự yêu mến của đông đảo độc giả. Cuốn sách liên tục đứng trong danh sách bán chạy của New York Times suốt 58 tuần với 6 triệu bản được bán ra trên toàn thế giới&nbsp;và dẫn đầu mục tiểu thuyết bán chạy của Amazon trong năm 2019.</p>
						<p>Delia Owens sinh năm 1949, là tác giả, nhà động vật học người Mỹ. Bên cạnh cuốn tiểu thuyết&nbsp;Xa ngoài kia nơi loài tôm hát, bà còn cùng chồng cũ viết ba cuốn sách về thế giới hoang dã tại châu Phi.</p>
						<p>"<strong>Xa ngoài kia nơi loài tôm hát</strong>" là một tiểu thuyết lãng mạn đầy lôi cuốn, đầy ắp hơi thở thiên nhiên đẹp đẽ và đau đớn. Lấy bối cảnh những năm 1950 về cô gái trẻ Kya Clark ở Bắc Crolina, Hoa Kỳ.&nbsp;Truyện kể về hành trình trưởng thành của Kya - cô bé khốn khổ, bị sự kì thị vây bọc cùng cái tên "cô gái đồng lầy" bởi cô sống một mình giữa thiên nhiên, chỉ kết nối với một vài người.&nbsp;</p>
						<p>Trong một căn lán nhỏ giữa đồng lầy, gia đình cô bé Kya 6 tuổi sống tách biệt hoàn toàn với cư dân của Vũng Barkley, hòa mình giữa thiên nhiên hoang dã trong khu rừng còn những bầy hươu, lũ chồn hoang, và lũ chim trời thường bay xáo động ở mé ngoài đồng lầy. Cha của Kya là một cựu chiến binh từ Thế chiến II, và cũng là một gã nát rượu vũ phu thường xuyên đánh đập mẹ cô bé cùng anh chị cô mỗi khi nổi cáu, trên Kya còn có hai người anh và hai người chị.</p>
						<p>Và rồi người mẹ quyết định rời bỏ ngôi nhà nhỏ ra đi, mang theo chiếc vali màu lam nổi bật giữa những vạt rừng màu xanh lục, bỏ Kya cùng các anh chị em cô bé ở lại. Những anh chị khác của cô bé, lần lượt cũng bỏ nhà ra đi vì không thể tiếp tục chịu đựng sự cộc cằn của người cha bất nhẫn. Mọi người rời đi hết, kể cả anh Jolie là người thân thiết nhất với Kya, chỉ để lại một mình cô bé 6 tuổi ở cùng một người cha nát rượu.</p>
						<p>Ở tình huống bất đắc dĩ đó, Kya phải học cách tự xoay sở cuộc sống của mình như tự nấu ăn theo những món mà mẹ cô thường hay nấu, hay tự dọn dẹp nhà cửa, giặt giũ quần áo cho sạch sẽ nếu như không muốn bị cha cô đánh. Mỗi tuần, cha sẽ đưa Kya 1 đô la, để cô bé trang trải sinh hoạt phí cho gia đình cả tuần. Nhưng có nhiều tuần, người cha đi biền biệt không trở về, Kya phải cắt những lá củ cải mẹ trồng ngoài vườn để luộc lên ăn qua bữa.&nbsp;</p>
						<p>Và rồi người cha bỏ đi không một lời từ biệt, người thân duy nhất còn lại rồi cũng rời bỏ Kya ra đi, khiến cô bé luôn dằn vặt tự hỏi mình đã làm gì sai để phải bị bỏ lại giữa đồng lầy như thế này?</p>
						<p>Và rồi, một ngày kia, người cha chỉ biết gào thét ấy bỏ đi không một lời từ biệt, người thân duy nhất còn lại rồi cũng rời bỏ Kya đi,&nbsp;khiến cô bé luôn dằn vặt tự hỏi mình đã làm gì sai để phải bị bỏ lại giữa đồng lầy như thế này?&nbsp;Kya phải tự bươn chải, dựa vào đồng lầy, vào cây cỏ, vỏ sò, lông vũ, lũ vẹn, lũ mòng biển mà sinh tồn...</p>
						<p style="text-align: center;"><img data-thumb="original" original-height="290" original-width="555" src="//bizweb.dktcdn.net/100/415/471/files/xa-ngoai-kia-noi-loai-tom-hat-delia-owens-2.jpg?v=1614174912478"></p>
						<p>Khi cô độc sống trong đầm lầy, Kya được bọn trẻ và cả cư dân Vũng Barkley đặt cho biệt danh là “cô gái đồng lầy”. Mọi người đồn đoán rằng cô bé là người vượn hay người sói, thoắt ẩn thoắt hiện trong rừng. Dù chính quyền cũng từng nỗ lực xuống vận động cha Kya cho cô bé đi học trước đây, nhưng việc tiếp xúc với quá nhiều người trong một môi trường chật hẹp và sự khinh miệt từ lũ bạn đồng trang lứa khiến Kya sợ hãi trốn học để chạy về đồng lầy – nơi cô bé được là chính mình giữa thiên nhiên hoang dại, với lũ mòng biển ngoài bờ biển mà cô bé vẫn thường rải bánh mì cho chúng ăn.</p>
						<p>7 tuổi, mù chữ, để sinh tồn khi mất nguồn chu cấp từ người cha đã bỏ đi biệt xứ, Kya đi bắt vẹm ngoài bờ biển và tự lái chiếc thuyền của cha để lại ra bến tàu để bán cho bác Jumpin’, đổi lấy tiền và nhu yếu phẩm thường dùng như bột ngô, diêm, xăng. Bác Jumpin’ là một người da đen, có một tiệm tạp hóa nhỏ nơi bến tàu. Vì cảm thương cho Kya bé nhỏ phải tự mình bươn chải mưu sinh, vợ của bác là bà Mable đã nhận cá xông khói của cô bé, đổi lại sẽ cho cô quần áo và một số thức ăn hằng tuần. Bối cảnh câu chuyện diễn ra vào thập niên 60-70 của nước Mỹ, khi đó dân da màu vẫn còn bị kỳ thị, và người da màu thì không được vào những quán ăn của người da trắng cũng như một số nơi như tòa án. Cô gái đồng lầy cũng được xếp vào nhóm bị cư dân của Vũng Barkley kỳ thị không khác gì người da màu.</p>
						<p>Cuộc đời của Kya lật sang chương mới khi cô bé được Tate Walker – một cậu bé có trái tim ấm áp, lớn hơn Kya vài tuổi – dạy cô bé học chữ. Kya biết Tate trong một lần lén lấy thuyền của cha chạy trên đầm lầy và bị lạc, rồi may mắn gặp Tate câu cá gần đó dẫn đường quay trở về. Những bài học vỡ lòng đầu tiên, Tate lấy một quyển sách để dạy Kya nhận diện mặt chữ, rồi học cách đánh vần, ghép từng từ lại với nhau.</p>
						<blockquote>
							<p>“<em>Em hông biết ngôn từ có thể chứa đựng nhiều điều đến vậy. Em không biết một câu có thể đầy ắp như thế.”</em></p>
							<p><em>Cậu mỉm cười. “Đó là một câu rất hay. Không phải câu từ nào cũng chứa đựng nhiều như vậy.”</em></p>
						</blockquote>
						<p>Từ khi biết đọc, Kya bắt đầu bước vào thế giới của sách vở, cô bé có thể đọc ngấu nghiến những cuốn sách về khoa học, sinh học mà Tate lấy từ mớ sách cũ thư viện và trường học bỏ đi cho Kya đọc. Tate và Kya đã lớn lên cùng nhau trong một thế giới bí mật giữa vùng đồng lầy, nơi chỉ có lũ diệc và mòng biển biết sự tồn tại của cả hai, ngay cả cha của Tate cũng không biết chuyện con trai mình thường vào đây mỗi tuần dạy Tate học chữ.</p>
						<p>Tate cũng là người chứng kiến sự trưởng thành của Kya khi cô bé lần đầu trải qua kỳ kinh nguyệt. Tình cảm của Tate dành cho Kya ban đầu chỉ là một người anh lớn dành cho cô em gái nhỏ chịu nhiều thiệt thòi, và rồi chuyển dần sang tình yêu nam nữ theo bản năng tự nhiên. Tate và Kya đã có một quãng thời gian yêu nhau nồng nhiệt và say đắm, đồng lầy cũng là nơi chứng kiến nụ hôn đầu của cả hai. Và trong một lần sắp nếm thử trái cấm, Tate đã kịp thời dừng lại đúng lúc vì không muốn làm tổn thương Kya, khi cô bé lúc ấy chỉ mới 15 tuổi</p>
						<p>Câu chuyện tình yêu nào cũng có những cao trào, khi Tate 19 tuổi phải rời Vũng Barkley để đi học đại học. Anh muốn trở thành một nhà nghiên cứu sinh học để quay trở lại nghiên cứu sinh vật vùng đồng lầy, nơi anh đã dành cả tuổi thơ và thời niên thiếu của mình cùng với Kya. Lời hứa hẹn quay trở lại rồi cuốn trôi theo con nước thủy triều rút, để cô gái đồng lầy nơi đây đợi anh biền biệt mấy năm trời. Và rồi Tate cũng bỏ Kya đi như mẹ, như cha, hay các anh chị em của cô bé, Kya bây giờ đã mất lòng tin vào con người. Cô phẫn uất, cô đau khổ, cô tuyệt vọng, nhiều lần cô chạy ra bãi biển nằm khóc, hay chạy vào rừng sâu với lũ diệc và bầy hươu. Cô hòa tan mình vào đồng lầy, trở thành một phần của đồng lầy, chỉ có ở giữa thiên nhiên, Kya mới thực sự là mình. Đồng lầy không bao giờ rời bỏ Kya.</p>
						<p>Tuy cách truyền thông về cuốn&nbsp;<strong>sách&nbsp;Xa ngoài kia nơi loài tôm hát&nbsp;</strong>nhấn mạnh vào chuyện một vụ án bí ẩn xảy ra giữa đồng lầy, nơi cái chết kỳ lạ của chàng trai Chase Andrews xảy ra với nhiều bí ẩn làm xôn xao cả thị trấn, và Kya là nghi can trực tiếp trong vụ án đó; nhưng với mình, đây lại là một cuốn tiểu thuyết trữ tình lãng mạn về chuyện con người có thể sống hòa hợp với thiên nhiên như thế nào, và thiên nhiên dạy chúng ta những bài học gì về quy luật cuộc sống. Như cô bé Kya khi quan sát cách lũ chim rừng, bọ ngựa hay đom đóm vào mùa động dục quyến rũ nhau như thế nào, cô nhìn ra được bản chất của lũ con trai trong vùng.</p>
						<p style="text-align: center;"><img data-thumb="original" original-height="301" original-width="555" src="//bizweb.dktcdn.net/100/415/471/files/xa-ngoai-kia-noi-loai-tom-hat-delia-owens-3.jpg?v=1614174894284"></p>
						<p>Khi Tate rời bỏ Kya ra đi, Chase Andrews xuất hiện như một con công đực với bộ lông rực rỡ thu hút ánh nhìn của Kya. Một thiếu nữ mới lớn, còn ngây thơ nên rất dễ rung động bởi những lời tán tỉnh ngọt ngào của Chase cùng tiếng kèn harmonica như rót mật của anh chàng. Và Kya đã trao cho Chase trái cấm cùng toàn bộ trái tim mình, mong chờ lời hứa hẹn về cuộc hôn nhân với anh, trong ngôi nhà và những đứa trẻ. Nhưng sự phản bội của Chase và sau đó là cái chết đầy bí ẩn của anh, người dân địa phương ngay lập tức đổ dồn nghi ngờ vào Kya, khiến cô vướng vào một loạt rắc rối khi bị xem là nghi can giết người.</p>
						<p>Chính tình yêu của Tate dành cho Kya, cùng tình thương của một số cư dân thị trấn đối với cô gái đồng lầy, khiến cho Kya hoang dại dần mở cửa trái tim mình để đón nhận tình cảm của mọi người. Khởi đầu từ một câu chuyện bi kịch nhưng kết thúc cuốn sách là một sự hạnh phúc ngập tràn trong tình thương và tình yêu, giữa con người với con người, và giữa con người với thiên nhiên.</p>
						<p>Rất lâu rồi mới có một cuốn tiểu thuyết nước ngoài làm mình cuốn hút như thế, mình đọc một mạch từ sáng tới chiều hết cuốn sách dày cộm khoảng 500 trang. Khi đi đến hồi kết của câu chuyện, dù happy ending nhưng cái kết có vài chi tiết tháo nút bất ngờ vẫn làm mình rơi nước mắt.&nbsp;<strong><em>Xa ngoài kia nơi loài tôm hát&nbsp;</em></strong>–&nbsp;một câu chuyện hay được kể bằng ngôn từ rất đẹp và có thể chạm đến được trái tim mình.</p>
